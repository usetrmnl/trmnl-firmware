void gen2_imgDrawText(uint32_t x, uint32_t y, const char *text,
                      uint8_t fg_nib, uint8_t bg_nib, uint8_t scale)
{
    if (!text || scale == 0) return;
    for (size_t ci = 0; text[ci]; ci++) {
        uint8_t c = (uint8_t)text[ci];
        if (c < 32 || c > 127) c = 32;
        const uint8_t *glyph = kFont8x8[c - 32];
        uint32_t cx = x + (uint32_t)ci * 8u * scale;
        for (uint32_t row = 0; row < 8u; row++) {
            uint8_t rowByte = glyph[row];
            for (uint32_t col = 0; col < 8u; col++) {
                uint8_t nib = ((rowByte >> (7u - col)) & 1u) ? fg_nib : bg_nib;
                uint32_t px = cx + col * scale;
                uint32_t py = y  + row * scale;
                for (uint32_t sy = 0; sy < scale; sy++)
                    for (uint32_t sx = 0; sx < scale; sx++)
                        gen2_imgWritePixel(px + sx, py + sy, nib);
            }
        }
    }
}
